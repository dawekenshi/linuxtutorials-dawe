function lightmode() {
    document.querySelector('body').style.background = "white";
    document.querySelector('.menu').style.background = "white";
    document.querySelector('.spanish').style.background = "white";
    document.querySelector('.english').style.background = "white";
    document.querySelector('.light-mode').style.background = "white";
    document.querySelector('.dark-mode').style.background = "white";
}

function darkmode() {
    document.querySelector('body').style.background = "black";
    document.querySelector('.menu').style.background = "black";
    document.querySelector('.spanish').style.background = "black";
    document.querySelector('.english').style.background = "black";
    document.querySelector('.light-mode').style.background = "black";
    document.querySelector('.dark-mode').style.background = "black";
}
